package ex02;

public class SamsungTV {
	
	public void powerOn() {
		System.out.println("SamsungTV의 전원을 킵니다.");
	}
	
	public void powerOff() {
		System.out.println("SamsungTV의 전원을 끕니다.");
	}
	
	public void volumeUp() {
		System.out.println("SamsungTV의 소리를 올립니다.");
	}
	
	public void volumeDown() {
		System.out.println("SamsungTV의 소리를 내립니다.");
	}
	
}
