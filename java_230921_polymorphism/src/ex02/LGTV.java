package ex02;

public class LGTV {
	
	public void turnOn() {
		System.out.println("LGTV의 전원을 킵니다.");
	}
	
	public void turnOff() {
		System.out.println("LGTV의 전원을 끕니다.");
	}
	
	public void soundUp() {
		System.out.println("LGTV의 소리를 올립니다.");
	}
	
	public void soundDown() {
		System.out.println("LGTV의 소리를 내립니다.");
	}
	
}
